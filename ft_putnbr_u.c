#include "ft_printf.h"

void	ft_putnbr_u(unsigned int nb, int *count)
{
	if (nb >= 10)
	{
		ft_putnbr(nb / 10, count);
		ft_putnbr(nb % 10, count);
	}
	else
		ft_putchar(nb + 48, count);
}
